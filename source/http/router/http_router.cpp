#include "http/router/http_router.h"
#include "http/router/route_item.h"

namespace obelisk::http {
    std::shared_ptr<http_response> http_router::handle(std::shared_ptr<http_request> request) {
        return std::shared_ptr<http_response>();
    }

    route_item &http_router::add_route(const std::string &path, const std::function<std::shared_ptr<http_response>(std::shared_ptr<http_request> &)> &handler) {
        return routes_.emplace_back(path, handler);
    }
} // obelisk